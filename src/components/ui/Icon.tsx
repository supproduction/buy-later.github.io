interface IconProps {
  path: string;
  className?: string;
}

/** Minimal stroke icon renderer — avoids pulling in an icon library. */
export function Icon({ path, className = 'h-5 w-5' }: IconProps) {
  return (
    <svg
      className={className}
      viewBox="0 0 24 24"
      fill="none"
      stroke="currentColor"
      strokeWidth={1.8}
      strokeLinecap="round"
      strokeLinejoin="round"
      aria-hidden="true"
    >
      <path d={path} />
    </svg>
  );
}
